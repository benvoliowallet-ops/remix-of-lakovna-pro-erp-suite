// Helper function to find RAL color by code
export function findRALColor(code: string): RALColor | undefined {
  return RAL_COLORS.find(c => c.code === code);
}

// Get RAL colors for combobox
export function getRALColorOptions() {
  return RAL_COLORS.map(c => ({
    value: c.code,
    label: `RAL ${c.code}`,
    name: c.name,
    hex: c.hex,
  }));
}

// Format RAL code with color name for display
export function formatRALWithName(ralCode: string, colorName?: string | null): string {
  if (ralCode === 'ZAKLAD') {
    return 'Základ';
  }
  
  const name = colorName || findRALColor(ralCode)?.name;
  return name ? `RAL ${ralCode} - ${name}` : `RAL ${ralCode}`;
}
